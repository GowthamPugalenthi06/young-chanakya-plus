  <h4 class="color-brand-1 mb-20" id="code-of-conduct">Code of Conduct for Young Chanakya</h4>

  <!-- Mentor Team -->
  <h5 class="color-brand-1 mt-4">1. Mentor Team</h5>
  <ul>
    <li><strong>Provide Constructive Feedback:</strong> Offer actionable, unbiased advice to students, startups, and other members.</li>
    <li><strong>Maintain Confidentiality:</strong> Respect the confidentiality of sensitive business and personal information shared by mentees.</li>
    <li><strong>Stay Engaged:</strong> Actively engage with your mentees, ensuring you offer meaningful guidance and support.</li>
    <li><strong>Set High Standards:</strong> Lead by example in terms of professionalism, ethics, and commitment.</li>
  </ul>

  <!-- Advisory Team -->
  <h5 class="color-brand-1 mt-4">2. Advisory Team</h5>
  <ul>
    <li><strong>Provide Expertise:</strong> Offer strategic advice based on your experience in business, investment, or academia.</li>
    <li><strong>Stay Transparent:</strong> Clearly communicate any conflicts of interest to avoid bias in your recommendations.</li>
    <li><strong>Support with Integrity:</strong> Offer guidance rooted in values of fairness, ethical behavior, and long-term sustainability.</li>
    <li><strong>Respect Boundaries:</strong> Understand the limits of your advisory role and respect decisions made by startup founders or management.</li>
  </ul>

  <!-- Academic Team -->
  <h5 class="color-brand-1 mt-4">3. Academic Team</h5>
  <ul>
    <li><strong>Promote Academic Integrity:</strong> Ensure that all educational materials are accurate, relevant, and free from plagiarism.</li>
    <li><strong>Foster Inclusive Learning:</strong> Create an environment where students feel welcomed, heard, and respected.</li>
    <li><strong>Engage with Students:</strong> Actively interact with students, answering questions and fostering curiosity.</li>
    <li><strong>Continuously Improve Curriculum:</strong> Update and refine the curriculum to meet industry standards and evolving trends.</li>
  </ul>

  <!-- VC Team -->
  <h5 class="color-brand-1 mt-4">4. VC Team</h5>
  <ul>
    <li><strong>Be Transparent:</strong> Communicate investment expectations and terms clearly to all involved parties.</li>
    <li><strong>Maintain Objectivity:</strong> Ensure all funding decisions are based on merit and potential, rather than personal bias or relationships.</li>
    <li><strong>Provide Value Beyond Funding:</strong> Offer strategic guidance, networking, and mentoring to your portfolio companies.</li>
    <li><strong>Respect Confidentiality:</strong> Handle financial, business, and personal data of startups with the highest level of confidentiality.</li>
  </ul>

  <!-- Management Intern -->
  <h5 class="color-brand-1 mt-4">5. Management Intern</h5>
  <ul>
    <li><strong>Respect Professionalism:</strong> Adhere to all workplace rules, and exhibit professionalism in your conduct and communication.</li>
    <li><strong>Maintain Confidentiality:</strong> Handle all internal and client information with discretion and confidentiality.</li>
    <li><strong>Be Proactive:</strong> Take initiative in your tasks, seeking learning opportunities and contributing effectively to the team.</li>
    <li><strong>Respect Teamwork:</strong> Collaborate with other team members, respecting their input and ideas.</li>
  </ul>

  <!-- Chanakyas Code of Conduct --><br>
  <h4 class="color-brand-1 mt-5 mb-3" id="licensing">Chanakyas Code of Conduct</h4>

  <!-- CEO & MDs -->
  <h5 class="color-brand-1 mt-4">1. CEO & MD’s</h5>
  <ul>
    <li><strong>Lead with Integrity:</strong> Uphold high standards of honesty, fairness, and professionalism in every decision and interaction.</li>
    <li><strong>Empower Your Team:</strong> Foster an inclusive and collaborative environment where your team members feel respected and heard.</li>
    <li><strong>Focus on Long-term Growth:</strong> Build your company with sustainability, ethics, and long-term value in mind.</li>
    <li><strong>Be a Role Model:</strong> Demonstrate commitment, accountability, and resilience — setting an example for others to follow.</li>
  </ul>

  <!-- Unicorn Founders -->
  <h5 class="color-brand-1 mt-4">2. Unicorn Founders</h5>
  <ul>
    <li><strong>Give Back to the Ecosystem:</strong> Share your success stories, lessons learned, and best practices with younger founders.</li>
    <li><strong>Promote Ethical Business Practices:</strong> Build your business around core values like transparency, accountability, and respect.</li>
    <li><strong>Invest in Future Leaders:</strong> Mentor upcoming entrepreneurs and provide them with opportunities for growth.</li>
    <li><strong>Stay Grounded:</strong> Despite your success, remain humble and committed to your community and team.</li>
  </ul>

  <!-- Executives -->
  <h5 class="color-brand-1 mt-4">3. Executives</h5>
  <ul>
    <li><strong>Exhibit Leadership Skills:</strong> Lead by example, motivating and guiding teams towards achieving business goals.</li>
    <li><strong>Maintain Open Communication:</strong> Foster a transparent and open communication channel between all levels of the organization.</li>
    <li><strong>Focus on Efficiency:</strong> Streamline operations and processes to maximize business growth and performance.</li>
    <li><strong>Be Solution-Oriented:</strong> Approach challenges with a problem-solving mindset, contributing to the company’s success.</li>
  </ul>

  <!-- Fortune 500’s -->
  <h5 class="color-brand-1 mt-4">4. Fortune 500’s</h5>
  <ul>
    <li><strong>Lead by Industry Example:</strong> Uphold the highest standards of corporate governance and ethics.</li>
    <li><strong>Collaborate with Startups:</strong> Partner with startups and entrepreneurs to help them scale and succeed by offering mentorship and resources.</li>
    <li><strong>Ensure Sustainability:</strong> Lead initiatives focused on social responsibility, sustainability, and corporate governance.</li>
    <li><strong>Inspire Innovation:</strong> Encourage a culture of innovation and foster an entrepreneurial mindset within your teams.</li>
  </ul>

  <!-- Membership Code of Conduct --><br>
  <h4 class="color-brand-1 mt-5 mb-3" id="product">Membership Code of Conduct</h4>

  <!-- Startup Membership -->
  <h5 class="color-brand-1 mt-4">1. Startup Membership</h5>
  <ul>
    <li><strong>Commit to Ethical Growth:</strong> Build your business ethically, without resorting to shortcuts or dishonest practices.</li>
    <li><strong>Collaborate with the Community:</strong> Engage with fellow founders and members for mutual growth, offering insights and support.</li>
    <li><strong>Be Transparent:</strong> Be honest with investors, partners, and customers regarding your business model, capabilities, and progress.</li>
  </ul>

  <!-- Volunteer Membership -->
  <h5 class="color-brand-1 mt-4">2. Volunteer Membership</h5>
  <ul>
    <li><strong>Be Proactive & Supportive:</strong> Take initiative in all assigned tasks and engage fully with the community.</li>
    <li><strong>Respect Others:</strong> Treat fellow volunteers and members with respect, kindness, and professionalism.</li>
    <li><strong>Honor Time Commitments:</strong> Be punctual and reliable in fulfilling your volunteer duties.</li>
  </ul>

  <!-- Student Membership -->
  <h5 class="color-brand-1 mt-4">3. Student Membership</h5>
  <ul>
    <li><strong>Be Open to Learning:</strong> Embrace new knowledge, and be active in discussions, events, and workshops.</li>
    <li><strong>Respect Mentors & Peers:</strong> Treat all mentors, coaches, and peers with respect and professionalism.</li>
    <li><strong>Act with Integrity:</strong> Honor commitments and deadlines, and give credit where it's due.</li>
  </ul>

  <!-- Business/Entrepreneur Membership -->
  <h5 class="color-brand-1 mt-4">4. Business / Entrepreneur Membership</h5>
  <ul>
    <li><strong>Drive Value Creation:</strong> Focus on building a sustainable, ethical business model that benefits society.</li>
    <li><strong>Mentor & Give Back:</strong> Share your experiences, challenges, and insights with younger entrepreneurs.</li>
    <li><strong>Be Transparent in Transactions:</strong> Engage with partners, clients, and investors in a transparent, honest, and professional manner.</li>
  </ul>

  <!-- Executive Membership -->
  <h5 class="color-brand-1 mt-4">5. Executive Membership</h5>
  <ul>
    <li><strong>Foster Leadership:</strong> Lead teams, mentor others, and drive innovation within your domain.</li>
    <li><strong>Commit to Excellence:</strong> Pursue excellence in every aspect of your work and demonstrate high standards of professional ethics.</li>
    <li><strong>Support the Community:</strong> Engage with Young Chanakya members, helping to create a network of growth, learning, and support.</li>
  </ul>
