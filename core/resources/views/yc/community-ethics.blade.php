  <h4 class="color-brand-1 mb-20" id="community-ethics">Young Chanakya Community Ethics</h4>
  <p><strong>Built on Trust. Led by Integrity. Empowered by Action.</strong></p>
  <p>At Young Chanakya, we are building India’s most elite Startup Business Leadership Network, where trust, transparency, and ethical behavior are foundational pillars.</p>
  <p>This community is more than a membership — it’s a shared mindset among founders, students, executives, and business leaders who believe in doing the right thing while building impactful ventures.</p>
  <p>The strength of the Young Chanakya ecosystem depends on how its members uphold ethics, not just in business, but in behavior, collaboration, and commitment to growth.</p>

  <h5 class="color-brand-1 mt-4 mb-2" id="licensing">Our Ethical Expectations for All Members</h5>
  <p>To protect the integrity of the Young Chanakya ecosystem, we expect every founder, member, partner, and participant to uphold the following principles:</p>

  <ul>
    <li><strong>1. Respect & Fairness:</strong> Treat co-founders, employees, peers, and volunteers with respect, fairness, and dignity. Acknowledge diversity of thought and foster inclusive environments.</li>

    <li><strong>2. Honest Communication:</strong> Be truthful in conversations with investors, mentors, partners, and the community. Do not engage in misleading, manipulative, or exaggerated claims during pitches or outreach.</li>

    <li><strong>3. Zero Tolerance for Harassment:</strong> Absolutely no harassment, bullying, or discrimination of any kind is tolerated within the community. Create safe spaces, whether offline or online, for meaningful dialogue and collaboration.</li>

    <li><strong>4. Integrity in Selling and Promotion:</strong> Refrain from using spammy, dishonest, or aggressive sales tactics. Avoid shilling for tokens, NFTs, events, political campaigns, or affiliate content under false pretenses.</li>

    <li><strong>5. Confidentiality is Sacred:</strong> Respect all off-the-record conversations, mentor advice, and inside community communications. Do not leak, forward, or misuse any private information without explicit consent.</li>

    <li><strong>6. Honor Your Word:</strong> Commit to handshake deals, spoken agreements, and signed contracts with full responsibility. Follow through on promises made to team members, customers, or community partners.</li>

    <li><strong>7. Professional Financial Conduct:</strong> Treat any investment or grant received with utmost responsibility — funds must be used solely for the growth of your venture. Avoid unethical practices in accounting, fundraising, or resource usage.</li>

    <li><strong>8. Security & Privacy First:</strong> Take user and company data privacy seriously. Act swiftly on any bugs, leaks, or vulnerabilities with transparency and urgency.</li>

    <li><strong>9. Protect the Young Chanakya Brand:</strong> Do not act in a way that tarnishes the reputation of Young Chanakya, its mentors, or its community members. Be mindful of public behavior, social media presence, and representation in events.</li>

    <li><strong>10. Truthfulness in Applications:</strong> Be honest in applications, interviews, and self-representations. False claims or fake achievements will lead to disqualification from all programs.</li>

    <li><strong>11. Operate in Good Faith:</strong> Build with passion, operate with clarity, and behave with professionalism in every interaction. Support fellow community members through collaboration, not competition.</li>
  </ul>

  <h5 class="color-brand-1 mt-4 mb-2" id='product'>Violation Consequences</h5>
  <p>We take community ethics seriously. If any member (or their company) is found to have violated the spirit or letter of this code (as determined by Young Chanakya leadership), the consequences may include:</p>
  <ul>
    <li>Revocation of Young Chanakya Founder/Member Status</li>
    <li>Removal from events, communities, digital platforms, and resources</li>
    <li>Permanent blacklisting from future programs and memberships</li>
    <li>Public notice (in extreme cases of fraud or harm)</li>
  </ul>
  <p><em>Note: Founders may also be held responsible for the unethical actions of a co-founder or employee, based on case-specific assessments.</em></p>

  <h5 class="color-brand-1 mt-4 mb-2" id="delivery">Our Promise to You</h5>
  <p>Young Chanakya will stand beside you through your toughest entrepreneurial moments — from failure to pivot, from struggle to breakthrough — as long as you remain ethical, honest, and aligned with the values of this tribe.</p>
  <p><strong>Build boldly. Lead with character.<br>You are not just building a startup.<br>You are building your legacy — the Chanakya way.</strong></p>
